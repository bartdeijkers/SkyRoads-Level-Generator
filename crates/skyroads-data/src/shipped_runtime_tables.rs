use crate::exe::{
    ExeDispatchEntry, ExeRuntimeDispatchTable, ExeRuntimeTables, ExeRuntimeU8Table,
    ExeShipRuntimeTables, DOS_SHIP_SHADOW_MASK_BYTES,
};

const SHIPPED_EXE_HEADER_BYTES: usize = 512;
const SHIPPED_EXE_READER_BASE_FILE_OFFSET: usize = 26848;
const TILE_CLASS_OFFSET: u16 = 0x0B77;
const DRAW_DISPATCH_OFFSET: u16 = 0x0B7F;

/// Runtime tables lifted once from the shipped DOS build so the native app
/// can keep the original behavior without requiring `SKYROADS.EXE` at startup.
pub fn shipped_runtime_tables() -> ExeRuntimeTables {
    let tile_class_file_offset =
        SHIPPED_EXE_READER_BASE_FILE_OFFSET + usize::from(TILE_CLASS_OFFSET);
    let draw_dispatch_file_offset =
        SHIPPED_EXE_READER_BASE_FILE_OFFSET + usize::from(DRAW_DISPATCH_OFFSET);

    ExeRuntimeTables {
        tile_class_by_low3: ExeRuntimeU8Table {
            offset: TILE_CLASS_OFFSET,
            image_offset: tile_class_file_offset - SHIPPED_EXE_HEADER_BYTES,
            file_offset: tile_class_file_offset,
            values: SHIPPED_TILE_CLASS_BY_LOW3.to_vec(),
        },
        draw_dispatch_by_type: ExeRuntimeDispatchTable {
            offset: DRAW_DISPATCH_OFFSET,
            image_offset: draw_dispatch_file_offset - SHIPPED_EXE_HEADER_BYTES,
            file_offset: draw_dispatch_file_offset,
            entries: SHIPPED_DRAW_DISPATCH_TARGETS
                .iter()
                .copied()
                .enumerate()
                .map(|(index, target)| ExeDispatchEntry {
                    index,
                    target,
                    target_label: dispatch_label(target),
                })
                .collect(),
        },
        ship: shipped_ship_runtime_tables(),
    }
}

pub fn shipped_ship_runtime_tables() -> ExeShipRuntimeTables {
    ExeShipRuntimeTables {
        screen_x_bias_by_lane: SHIPPED_SHIP_SCREEN_X_BIAS_BY_LANE,
        surface_height_by_dispatch_kind: SHIPPED_SHIP_SURFACE_HEIGHT_BY_DISPATCH_KIND,
        thrust_phase_by_cycle: SHIPPED_SHIP_THRUST_PHASE_BY_CYCLE,
        shadow_masks: SHIPPED_SHIP_SHADOW_MASKS,
    }
}

fn dispatch_label(target: u16) -> Option<&'static str> {
    match target {
        0x2E50 => Some("draw_type_0"),
        0x303D => Some("draw_type_1"),
        0x2E9F => Some("draw_type_2"),
        0x2EE1 => Some("draw_type_3"),
        0x2F3C => Some("draw_type_4"),
        0x2FB0 => Some("draw_type_5"),
        0x3AAD => Some("noop"),
        _ => None,
    }
}

